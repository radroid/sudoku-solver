def solve_problem(prob_array):
    """Solves the given prob_array."""
    solver = SudokuSolver()
    t0 = time()
    solver.set_board(prob_array)
    solver.solve_board()
    print(f"Time taken to solve: {time() - t0: 0.3f}s")
    print(solver.board, '\n')


